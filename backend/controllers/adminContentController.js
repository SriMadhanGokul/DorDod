// ─── GOALS ───────────────────────────────────────────────────────────────────
const getAllGoals = async (req, res) => {
  try {
    const { userId, status } = req.query;
    const filter = {};
    if (userId) filter.user = userId;
    if (status && status !== 'All') filter.status = status;
    const goals = await Goal.find(filter).populate('user','name email').sort({ createdAt: -1 }).limit(100);
    res.status(200).json({ success: true, data: goals });
  } catch (err) { res.status(500).json({ success: false, message: 'Failed to fetch goals' }); }
};

const updateGoal = async (req, res) => {
  try {
    const goal = await Goal.findByIdAndUpdate(req.params.id, req.body, { new: true });
    if (!goal) return res.status(404).json({ success: false, message: 'Goal not found' });
    res.status(200).json({ success: true, message: 'Goal updated!', data: goal });
  } catch (err) { res.status(500).json({ success: false, message: 'Failed to update goal' }); }
};

const deleteGoal = async (req, res) => {
  try {
    await Goal.findByIdAndDelete(req.params.id);
    res.status(200).json({ success: true, message: 'Goal deleted!' });
  } catch (err) { res.status(500).json({ success: false, message: 'Failed to delete goal' }); }
};


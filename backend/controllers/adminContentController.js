const deleteComment = async (req, res) => {
  try {
    const post = await Post.findById(req.params.postId);
    if (!post) return res.status(404).json({ success: false, message: 'Post not found' });
    post.comments = post.comments.filter(c => c._id.toString() !== req.params.commentId);
    await post.save();
    res.status(200).json({ success: true, message: 'Comment deleted!' });
  } catch (err) { res.status(500).json({ success: false, message: 'Failed to delete comment' }); }
};

// ─── DEV PLAN ─────────────────────────────────────────────────────────────────
const addDevPlanRec = async (req, res) => {
  try {
    const { userId, title, type, duration, priority, reason } = req.body;
    let plan = await DevPlan.findOne({ user: userId });
    if (!plan) plan = await DevPlan.create({ user: userId, recommendations: [], milestones: [] });
    plan.recommendations.push({ title, type: type || 'Custom', duration: duration || '1 hour', priority: priority || 'medium', reason: reason || '', completed: false, icon: 'FaRocket' });
    await plan.save();
    res.status(201).json({ success: true, message: 'Recommendation added!', data: plan });
  } catch (err) { res.status(500).json({ success: false, message: 'Failed to add recommendation' }); }
};

const removeDevPlanRec = async (req, res) => {
  try {
    const plan = await DevPlan.findOne({ user: req.params.userId });
    if (!plan) return res.status(404).json({ success: false, message: 'Dev plan not found' });
    plan.recommendations = plan.recommendations.filter(r => r._id.toString() !== req.params.recId);
    await plan.save();
    res.status(200).json({ success: true, message: 'Recommendation removed!', data: plan });
  } catch (err) { res.status(500).json({ success: false, message: 'Failed to remove recommendation' }); }
};

// ─── NOTIFICATIONS ────────────────────────────────────────────────────────────
const sendNotification = async (req, res) => {
  try {
    const { userId, title, message, type } = req.body;
    if (!title || !message) return res.status(400).json({ success: false, message: 'Title and message required' });

    if (userId) {
      // User-specific
      await Notification.create({ user: userId, title, message, type: type || 'info', global: false });
    } else {
      // Global — send to all users
      const users = await User.find({ role: 'user' }).select('_id');
      await Notification.insertMany(users.map(u => ({ user: u._id, title, message, type: type || 'info', global: true })));
    }

    res.status(201).json({ success: true, message: userId ? 'Notification sent to user!' : `Notification sent to all users!` });
  } catch (err) { res.status(500).json({ success: false, message: 'Failed to send notification' }); }
};

const getNotifications = async (req, res) => {
  try {
    const notifs = await Notification.find()
      .populate('user', 'name email')
      .sort({ createdAt: -1 })
      .limit(50);
    res.status(200).json({ success: true, data: notifs });
  } catch (err) { res.status(500).json({ success: false, message: 'Failed to fetch notifications' }); }
};

